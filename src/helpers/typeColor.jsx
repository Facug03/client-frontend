  if (type === 'grass') return '#84d45e'
  if (type === 'fire') return '#d31710'
  if (type === 'poison') return '#7b2f91'
  if (type === 'flying') return '#a4e4d5'
  if (type === 'bug') return '#729f3f'
  if (type === 'fighting') return '#bb5544'
  if (type === 'ground') return '#ddbb55'
  if (type === 'rock') return '#a38c21'
  if (type === 'ghost') return '#444477'
  if (type === 'steel') return '#999999'
  if (type === 'water') return '#3399ff'
  if (type === 'electric') return '#eed535'
  if (type === 'psychic') return '#ff5599'
